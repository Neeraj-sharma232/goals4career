            <h2 class="text-xl text-[#00276A] font-semibold mt-4">Stick To The Point:</h2>
            <p class="mb-2">Employers have a busy schedule, so don’t expect them to read through a long resume. Ideally, resumes should be of one page, or of two pages only if absolutely necessary, to describe relevant experience.</p>
            <h2 class="text-xl text-[#00276A] font-semibold mt-4">Words Count</h2>
            <p class="mb-2">Use of language is extremely important; you need to sell yourself to an employer quickly and efficiently. Address your potential employer’s needs with a clearly written, compelling resume. Avoid large paragraphs (five or six lines). If you provide small, digestible pieces of information, your resume will be read. Use action verbs. Verbs such as “developed”, “managed”, and “designed” emphasize your accomplishments. Don’t use declarative sentences like “I developed the …” or “I assisted in … leave out the “I”. Avoid passive constructions, such as “was responsible for managing”. Just say, “managed”: that sounds stronger and more active.</p>
