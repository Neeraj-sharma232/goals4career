            <p class="mb-2">Check your resume for correct grammar and spelling – evidence of good communication skills and attention to detail. Nothing can ruin your chances of getting a job faster than submitting a resume filled with preventable mistakes. Make your resume easy on the eye. Use normal margins (1″ on the top and bottom, 1.25″ on the sides) and don’t cram your text on the page. Allow for some space between the different sections. Avoid unusual or exotic fonts. Preferred fonts: Arial and Times Roman & Verdana.</p>




