            </h2>
            <ul style="list-style-type: disc;">
                <li class="mb-2">Periods at the end of all full sentences.</li>
                <li class="mb-2">Be consistent in your use of punctuation.</li>
                <li class="mb-2">Always put periods and commas within quotation marks.</li>
                <li class="mb-2">Avoid using exclamation points.
                </li>
            </ul>
            <h2 class="text-xl text-[#00276A] font-semibold mt-4">Grammatical Mistakes :</h2>
            <h2 class="text-xl text-[#00276A] font-semibold mt-4">Grammar hang-ups to watch for :
            </h2>
            <ul style="list-style-type: disc;">
                <li class="mb-2">Do not switch tenses within your resume.</li>
                <li class="mb-2">The duties you currently perform should be in present tense (i.e., write reports)</li>
                <li class="mb-2">Duties you may have performed at past jobs should be in past tense (i.e., wrote reports).</li>
                <li class="mb-2">Capitalize all proper nouns.</li>
                <li class="mb-2">When expressing numbers, write out all numbers between one and nine (i.e., one, five, seven),but use numerals for all numbers 10 and above (i.e., 10, 25, 108).</li>
