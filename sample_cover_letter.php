            <h2 class="text-4xl text-[#19468A] font-semibold my-6">Sample Cover Letter</h2>
            <p class="mb-4 text-sm">Cover letters for freshers should have points that highlight their skills, qualifications, and strengths. It is important to understand the kind of role you are applying for, the company, the objectives of the company and the skills they are looking for, before applying. This helps in framing a good cover letter for the role. </p>
            <p class="mb-4 font-bold">Given below are the main steps that one could follow while writing a cover letter for freshers:</p>

            <p class="mb-4 font-bold">Greeting</p>
            <p class="mb-4">Always start by addressing the person who is hiring, preferably by name. If you are unsure of the name, you can stick to a generic greeting like – “Dear Hiring Manager”.</p>
            <p class="mb-4 font-bold">Introduction</p>
            <p class="mb-4">Introduction is key in a cover letter as it forms the first impression. So ensure to keep it short and crisp yet convey important information. Do not forget to mention why you are interested in the position you are applying for. Also, mention the organization you are applying to and the name of the position.</p>
            <p class="mb-4 font-bold">Qualifications</p>

            <p class="mb-4">Mention your relevant qualification and skills, including your education or any other related internships or courses. Highlight how your skills make you the right fit for the position you are applying for.</p>
            <p class="mb-4 font-bold">Experience</p>
            <p class="mb-4">If you have any previous relevant experience related to the position, such as some projects or volunteer work, you can mention it in this section. You can explain how the experience can help you in the position you are applying for.</p>
            <p class="mb-4 font-bold">Enthusiasm</p>
            <p class="mb-4">While sharing all details is necessary, it is also important to show how enthusiastic you are about joining the company and taking up the role. Explain why this opportunity aligns with your career goals.</p>
            <p class="mb-4 font-bold">Closing of letter</p>
            <p class="mb-4">The cover letter should always end with thanking the hiring manager of the company for considering your application. Do not forget to add your contact information including your phone number and email address.</p>
            <p class="mb-4 font-bold">Proofread</p>
            <p class="mb-4">It is imperative that your cover letter should be free of any spelling or grammar errors. Proof read the entire cover letter once you finish writing. Even if it’s well-written, by making it error-free, your chances of getting noticed can get higher.</p>
            <p class="mb-4">Remember, you should tailor your cover letter to the specific company and role you are applying for. Spend enough time to research about the position and company to highlight your skills that would make you the right fit.</p>
            <h3 class="text-2xl font-semibold mb-4">Tips and Tricks to Write a Job Application Letter</h3>
            <p class="mb-4">Now that you know what are the basic things to be included in a cover letter, here are some tips and tricks that you can keep in mind while writing a job application letter.</p>
            <p class="mb-4 font-bold">Customize Your Letter</p>
            <p class="mb-4">Your job application letter should be customized according to the specific company and position you are applying to. This shows that you are genuinely interested in the role and have done a good amount of research.</p>
            <p class="mb-4 font-bold">Keep It Shortr</p>
            <p class="mb-4">Your job application letter should be to the point. Keep it concise and do not let it exceed a single page, highlighting your qualifications and experience.</p>
            <p class="mb-4 font-bold">Highlight Your Strengths</p>
            <p class="mb-4">Highlight your strengths: Focus on your strengths and how they align with the requirements of the position. Provide concrete examples of your achievements and skills to demonstrate your fit for the role.</p>
            <p class="mb-4 font-bold">Use Keywords</p>
            <p class="mb-4">Use keywords from the job description in your application letter. This shows that you have done your research and read the description and that you have understood the role’s requirements.</p>
            <p class="mb-4 font-bold">Keep the tone professional</p>
            <p class="mb-4">Use a professional tone in your application later, stick to the format for your job application letter. Do not use a causal language and preferably use a traditional letter format.</p>
            <p class="mb-4 font-bold">Add a call to action</p>
            <p class="mb-4">Close your job application letter with a call to action, either asking to arrange for an interview or to meet to discuss your qualifications for a position.</p>
            <p class="mb-4">Your job application letter is your foremost impression with a potential employer. Ensure you take your time and craft a good application letter if you are interested in the position and company.</p>


            <h3 class="text-2xl font-semibold mb-4">Examples/samples of cover letter</h3>
            <p class="mb-4">Here are sample cover letters for freshers that you can use as a reference:</p>

            <p class="mb-4 font-semibold">Sample Cover Letter 1:</p>
            <p class="mb-4">Dear [Hiring Manager’s Name],</p>
            <p class="mb-4">I am writing to express my interest in the [Job Title] position at [Company Name]. As a recent graduate with a [Degree] in [Field], I am eager to bring my skills and enthusiasm to a dynamic and fast-paced work environment.</p>
            <p class="mb-4">Throughout my studies, I have gained a solid understanding of [Skill/Subject Relevant to the Job]. I have also had the opportunity to apply this knowledge through various internships, including my most recent role at [Previous Company/Organization]. In this role, I was responsible for [Key Responsibility] and was able to demonstrate my ability to [Key Achievement]. </p>
            <p class="mb-4">At [Company Name], I am particularly impressed by the company’s commitment to [Company Value/Mission]. I am confident that my passion for [Skill/Field] and my experience in [Related Experience] would make me a valuable asset to your team.</p>
            <p class="mb-4">Thank you for considering my application. I would welcome the opportunity to discuss my qualifications in more detail and how I can contribute to [Company Name]. Please find my resume attached for your review. </p>
            <p class="mb-4">Sincerely,</p>
            <p class="mb-4">[Your Name] </p>


